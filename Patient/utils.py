def is_compatible(donor_blood_type, patient_blood_type):
    compatibility = {
        'A-': ['A-', 'A+', 'AB-', 'AB+'],
        'A+': ['A+', 'AB+'],
        'B-': ['B-', 'B+', 'AB-', 'AB+'],
        'B+': ['B+', 'AB+'],
        'AB-': ['AB-', 'AB+'],
        'AB+': ['AB+']
    }
    return patient_blood_type in compatibility.get(donor_blood_type, [])
